package org.ntnu.k2.g2.quizmaker.Data;

public class Team {
    private String teamName;
    private int score;

    public Team(){}

    public String getTeamName() {
        return teamName;
    }

    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
